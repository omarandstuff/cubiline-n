using UnityEngine;
using System.Collections;

public class NavigationMenuButton : MonoBehaviour
{

	//////////////////////////////////////////////////////////////
	///////////////////////// COMPONENTS /////////////////////////
	//////////////////////////////////////////////////////////////
	public MenuController parentMenu;
	public enum BUTTON_SIDE { RIGHT, LEFT }
	public BUTTON_SIDE buttonSide;

	////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////// MONO BEHAVIOR /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	void OnMouseDown()
	{
		GetComponent<EaseScale>().easeFace = EaseVector3.EASE_FACE.OUT;
	}

	void OnMouseUp()
	{
		GetComponent<EaseScale>().easeFace = EaseVector3.EASE_FACE.IN;
	}

	void OnMouseUpAsButton()
	{
		if(buttonSide == BUTTON_SIDE.LEFT)
			parentMenu.TurnLeft();
		else
			parentMenu.TurnRight();
	}
}
