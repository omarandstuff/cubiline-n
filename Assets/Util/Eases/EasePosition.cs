
public class EasePosition : EaseVector3
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////// MONO BEHAVIOR /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		Reset();
	}

	void FixedUpdate()
	{
		UpdateEase();
		transform.localPosition = currentValues;
	}

	public override void Reset()
	{
		base.Reset();
		if (forceInitialFace)
			transform.localPosition = currentValues;
	}
}
