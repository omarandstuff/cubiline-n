using UnityEngine;

public class EaseRotation : EaseVector3
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////// MONO BEHAVIOR /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		Reset();
	}

	void FixedUpdate()
	{
		UpdateEase();
		transform.localRotation = Quaternion.Euler(currentValues);
	}

	public override void Reset()
	{
		base.Reset();
		if (forceInitialFace)
			transform.localRotation = Quaternion.Euler(currentValues);
	}
}
