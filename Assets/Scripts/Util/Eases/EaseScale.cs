using UnityEngine;
using System.Collections;

public class EaseScale : EaseVector3
{

	////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////// MONO BEHAVIOR /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	void Start()
	{
		Reset();
	}

	void FixedUpdate()
	{
		UpdateEase();
		transform.localScale = currentValues;
	}

	public override void Reset()
	{
		base.Reset();
		if (forceInitialFace)
			transform.localScale = currentValues;
	}
}
