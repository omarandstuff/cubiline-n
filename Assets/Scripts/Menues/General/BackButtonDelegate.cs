using UnityEngine;
using System.Collections;

public class BackButtonDelegate : MonoBehaviour
{

	//////////////////////////////////////////////////////////////
	///////////////////////// COMPONENTS /////////////////////////
	//////////////////////////////////////////////////////////////
	public MenuController parentMenu;

	////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////// MONO BEHAVIOR /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	void OnMouseDown()
	{
		GetComponent<EaseScale>().easeFace = EaseVector3.EASE_FACE.OUT;
	}

	void OnMouseUp()
	{
		GetComponent<EaseScale>().easeFace = EaseVector3.EASE_FACE.IN;
	}

	void OnMouseUpAsButton()
	{
		parentMenu.Back();
	}
}
