using UnityEngine;
using UnityEngine.UI;

public class EaseScore : MonoBehaviour
{
	//////////////////////////////////////////////////////////////
	//////////////////////// PARAMETERS //////////////////////////
	//////////////////////////////////////////////////////////////

	public float scoreSmoothTime = 0.1f;
	public uint score
	{
		get
		{
			return totalScore;
		}
		set
		{
			totalScore = value;
			targetScore = totalScore + 0.5f;
		}
	}

	//////////////////////////////////////////////////////////////
	////////////////////// CONTROL VARIABLES /////////////////////
	//////////////////////////////////////////////////////////////

	private uint totalScore;
	private float targetScore;
	private float currentScore;
	private float velocity = 0;
	

	////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////// MONO BEHAVIOR /////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	void FixedUpdate()
	{
		currentScore = Mathf.SmoothDamp(currentScore, targetScore, ref velocity, scoreSmoothTime);
		GetComponent<Text>().text = ((int)currentScore).ToString("D" + 2);
	}
}
